export type TranslationKey = keyof (typeof dictionaries)['en'];
type LooseKey = TranslationKey | (string & {});

interface LanguageContextValue {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: LooseKey, params?: Record<string, string | number>) => string;
  dir: 'rtl' | 'ltr';
}

const LanguageContext = createContext<LanguageContextValue | null>(null);

const STORAGE_KEY = 'jeen-language';

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    const stored = localStorage.getItem(STORAGE_KEY);
    return stored === 'en' || stored === 'he' ? stored : 'he';
  });

  const dir = language === 'he' ? 'rtl' : 'ltr';

  useEffect(() => {
    document.documentElement.dir = dir;
    document.documentElement.lang = language;
    localStorage.setItem(STORAGE_KEY, language);
  }, [dir, language]);

  const value = useMemo<LanguageContextValue>(
    () => ({
      language,
      setLanguage,
      dir,
      t: (key, params) => {
        let str: string = (dictionaries[language] as Record<string, string>)[key] ?? key;
        if (params) {
          for (const [name, val] of Object.entries(params)) {
            str = str.replaceAll(`{${name}}`, String(val));
          }
        }
        return str;
      },
    }),
    [language, dir],
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage(): LanguageContextValue {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useLanguage must be used within LanguageProvider');
  return ctx;
}
